#              2 Number Calculater

a=int(input("Enter of a: "))
operator=(input("Enter simbol: "))
b=int(input("Enter of b :"))

if(operator == "+"):
    print("sum of a and c",a+b)
elif(operator == "-"):
    print("sum of a and c",a-b)
elif(operator == "*"):
    print("sum of a and c",a*b)
elif(operator == "/"):
    print("sum of a and c",a/b)


